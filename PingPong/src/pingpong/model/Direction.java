package pingpong.model;

/**
 * Die Direction-Enum legt fest, in welche Richtung ein Paddle bewegt wird.
 * UP   = Paddle bewegt sich nach oben,
 * DOWN = Paddle bewegt sich nach unten,
 * NONE = Paddle bleibt stehen.
 */
public enum Direction {
    UP,    // Nach oben bewegen
    DOWN,  // Nach unten bewegen
    NONE   // Keine Bewegung
}

